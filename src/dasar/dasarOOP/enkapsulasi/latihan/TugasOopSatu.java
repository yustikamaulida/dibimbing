package dasar.dasarOOP.enkapsulasi.latihan;

public class TugasOopSatu {

	public static String rambut, kulit, mata, sifat;
	
	public void sinta() {
		rambut = "Ikal";
		kulit = "Hitam";
		mata = "Hitam";
		sifat = "Ngeselin";

		System.out.println("Sinta :" + "\nrambut : " + rambut + "\nkulit : " + kulit + "\nmata : " + mata + "\nsifat : " + sifat);
	}

	public void bima() {
		rambut = "Lurus";
		kulit = "Putih";
		mata = "Biru";
		sifat = "Menyenangkan";
		System.out.println("Bima :" + "\nrambut : " + rambut + "\nkulit : " + kulit + "\nmata : " + mata + "\nsifat : " + sifat);

	}

	public void narukoto() {
		rambut = "Pendek";
		kulit = "Coklat";
		mata = "Hitam";
		sifat = "Baik";
		System.out.println("Narukoto :" + "\nrambut : " + rambut + "\nkulit : " + kulit + "\nmata : " + mata + "\nsifat : " + sifat);

	}

}
